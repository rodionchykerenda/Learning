// MARK: -  BubbleSort
func bubbleSort(_ array: inout [Int]) {
    var swap = true
    while swap {
        swap = false
        for i in 0..<array.count - 1 {
            if array[i] > array[i + 1] {
                let temp = array[i + 1]
                array[i + 1] = array[i]
                array[i] = temp
                swap = true
            }
        }
    }
}

// MARK: -  QuickSort
